/// Represents an event on a chronological timeline.
class TimelineEvent {
  /// The title of the event.
  final String title;

  /// The date of the event as a display string.
  final String date;

  /// A brief description of the event.
  final String description;

  /// The year of the event (negative for BCE), used for sorting.
  final int year;

  /// Creates a [TimelineEvent] instance.
  const TimelineEvent({
    required this.title,
    required this.date,
    required this.description,
    required this.year,
  });
}
